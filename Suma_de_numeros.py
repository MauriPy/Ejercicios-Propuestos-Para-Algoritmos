# 1) Suma de números: Escribe un algoritmo que calcule la suma de dos números ingresados por el usuario.

# Pedimos al usuario los 2 numeros.
num_1 = int(input('Ingrese el primer numero: '))
num_2 = int(input('Ingrese el segundo numero: '))

# Calculamos la suma de los numeros en una nueva variable llamada suma.
suma = num_1 + num_2

# Mostramos en pantalla el resultado de la suma.
print(f'La suma del numero {num_1} + {num_2} es: {suma}')