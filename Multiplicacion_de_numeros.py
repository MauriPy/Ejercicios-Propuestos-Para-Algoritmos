# 2) Multiplicación de números: Crea un algoritmo que multiplique dos números ingresados por el usuario.

# Pedimos al usuario los 2 numeros.
num_1 = int(input('Ingrese el primer numero: '))
num_2 = int(input('Ingrese el segundo numero: '))

# Calculamos la multiplicacion de los numeros en una nueva variable llamada multiplicacion.
multiplicacion = num_1 * num_2

# Mostramos en pantalla el resultado de la multiplicacion.
print(f'La suma del numero {num_1} * {num_2} es: {multiplicacion}')